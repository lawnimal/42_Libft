#include "libft.h"

void	ft_putnbr_fd(int n, int fd)
{
	char	i;

	i = 0;
	if (n == -2147483648)
	{
		write(fd, "-2147483648", 11);
	}
	else if (n < 0)
	{
		write(fd, "-", 1);
		ft_putnbr_fd(-n, fd);
	}
	else if (n >= 10)
	{
		ft_putnbr_fd(n / 10, fd);
		i = n % 10 + 48;
		write(fd, &i, 1);
	}
	else
	{
		i = n % 10 + 48;
		write(fd, &i, 1);
	}
}
